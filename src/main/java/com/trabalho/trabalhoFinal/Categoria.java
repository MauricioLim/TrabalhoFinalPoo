/**
 * Enum que representa as categorias possíveis de lançamentos financeiros.
 * Categorias de receita: SALARIO, DECIMO_TERCEIRO, FERIAS, OUTRAS_RECEITAS.
 * Categorias de despesa: ALIMENTACAO, TRANSPORTE, RESIDENCIA, SAUDE, EDUCACAO, ENTRETENIMENTO, OUTRAS_DESPESAS.
 */
public enum Categoria {
    // Receitas
    SALARIO,
    DECIMO_TERCEIRO,
    FERIAS,
    OUTRAS_RECEITAS,

    // Despesas
    ALIMENTACAO,
    TRANSPORTE,
    RESIDENCIA,
    SAUDE,
    EDUCACAO,
    ENTRETENIMENTO,
    OUTRAS_DESPESAS
}
